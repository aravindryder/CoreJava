package com.CoreJava;

public class Iteration {

	public static void main(String[] args) {

		int i = 5;
		int j = 8;
		
		System.out.println(i++);
		System.out.println(i);
		System.out.println(++i);
		
		System.out.println(j--);
		System.out.println(j);
		System.out.println(--j);
		System.out.println(--j);
	}
}